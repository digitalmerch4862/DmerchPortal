const toProduct = (row: string): ProductItem | null => {
  const cleaned = row.trim();
  if (!cleaned) {
    return null;
  }

  const tabIndex = cleaned.lastIndexOf('\t');
  if (tabIndex > -1) {
    const name = cleaned.slice(0, tabIndex).trim();
    const amountRaw = cleaned.slice(tabIndex + 1).trim();
    const amount = Number(amountRaw);
    if (!name || Number.isNaN(amount)) {
      return null;
    }

    return {name, amount};
  }

  const fallback = cleaned.match(/^(.*?)(\d+(?:\.\d+)?)$/);
  if (!fallback) {
    return null;
  }

  const name = fallback[1].trim();
  const amount = Number(fallback[2]);
  if (!name || Number.isNaN(amount)) {
    return null;
  }

  return {name, amount};
};

const parsedProducts = PRODUCT_ROWS.split('\n')
  .map((line) => toProduct(line))
  .filter((item): item is ProductItem => Boolean(item));

const dedupedMap = new Map<string, ProductItem>();
for (const item of parsedProducts) {
  const dedupeKey = `${item.name.trim().toLowerCase()}::${item.amount}`;
  if (!dedupedMap.has(dedupeKey)) {
    dedupedMap.set(dedupeKey, item);
  }
}

export const productCatalog = Array.from(dedupedMap.values()).sort((a, b) => a.name.localeCompare(b.name));

export const findProductByName = (name: string) => {
  const normalized = name.trim().toLowerCase();
  return productCatalog.find((item) => item.name.toLowerCase() === normalized) ?? null;
};
